package com.company;

public class Main {

    public static void main(String[] args) {
        int kek = 5;
        int lol = kek + 2;
        double piy = (double) lol / kek;
        System.out.println(" Сумма = " + (kek + lol));
        System.out.println("Разность = " + (kek - lol));
        System.out.println("Произведение = " + (kek * lol));
        System.out.println("Частное = " + (lol / kek));
        System.out.println("Дробное частное = " + piy);
    }
}
